/** A deterministic random source in [0, 1); lets generator runs be reproduced in tests. */
export type Rng = () => number;

/** Mulberry32 – small, fast, good-enough distribution for weighted picks. */
export function createSeededRng(seed: number): Rng {
  let state = seed >>> 0;
  return () => {
    state = (state + 0x6d2b79f5) >>> 0;
    let t = state;
    t = Math.imul(t ^ (t >>> 15), t | 1);
    t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}
